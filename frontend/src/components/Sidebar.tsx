import { NavLink, useParams } from 'react-router'
import { useQuery } from '@tanstack/react-query'
import { api } from '../lib/api.js'

const navItems = [
  { label: 'Canonical Fields', path: 'canonical', icon: FieldsIcon },
  { label: 'Systems', path: 'systems', icon: SystemsIcon },
  { label: 'Mappings', path: 'mappings', icon: MappingsIcon },
  { label: 'Interfaces', path: 'interfaces', icon: InterfacesIcon },
]

export function Sidebar() {
  const { workspaceId } = useParams()

  const { data: workspaces } = useQuery({
    queryKey: ['workspaces'],
    queryFn: () => api.workspaces.list(),
  })

  const currentWorkspace = workspaces?.items.find((w) => w.id === workspaceId)

  return (
    <aside className="w-64 bg-gray-900 text-gray-300 flex flex-col h-full shrink-0">
      {/* Logo / App title */}
      <div className="px-4 py-5 border-b border-gray-800">
        <NavLink to="/" className="text-white font-bold text-lg">
          Interface Manager
        </NavLink>
      </div>

      {/* Workspace selector */}
      {workspaceId && (
        <div className="px-4 py-3 border-b border-gray-800">
          <p className="text-xs text-gray-500 uppercase tracking-wider mb-1">Workspace</p>
          <NavLink
            to={`/workspaces/${workspaceId}`}
            className="text-sm font-medium text-white hover:text-blue-400 transition-colors"
          >
            {currentWorkspace?.name ?? 'Loading...'}
          </NavLink>
        </div>
      )}

      {/* Navigation links */}
      {workspaceId && (
        <nav className="flex-1 px-2 py-4 space-y-1 overflow-y-auto">
          {navItems.map((item) => (
            <NavLink
              key={item.path}
              to={`/workspaces/${workspaceId}/${item.path}`}
              className={({ isActive }) =>
                `flex items-center gap-3 px-3 py-2 rounded-md text-sm font-medium transition-colors ${
                  isActive
                    ? 'bg-gray-800 text-white'
                    : 'text-gray-400 hover:bg-gray-800 hover:text-white'
                }`
              }
            >
              <item.icon />
              {item.label}
            </NavLink>
          ))}
          <div className="pt-4 mt-4 border-t border-gray-800">
            <NavLink
              to={`/workspaces/${workspaceId}/settings`}
              className={({ isActive }) =>
                `flex items-center gap-3 px-3 py-2 rounded-md text-sm font-medium transition-colors ${
                  isActive
                    ? 'bg-gray-800 text-white'
                    : 'text-gray-400 hover:bg-gray-800 hover:text-white'
                }`
              }
            >
              <SettingsIcon />
              Settings
            </NavLink>
          </div>
        </nav>
      )}

      {/* Non-workspace nav */}
      {!workspaceId && (
        <nav className="flex-1 px-2 py-4">
          <NavLink
            to="/"
            end
            className={({ isActive }) =>
              `flex items-center gap-3 px-3 py-2 rounded-md text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-gray-800 text-white'
                  : 'text-gray-400 hover:bg-gray-800 hover:text-white'
              }`
            }
          >
            <WorkspacesIcon />
            Workspaces
          </NavLink>
        </nav>
      )}
    </aside>
  )
}

